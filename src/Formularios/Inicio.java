    public void CambiarPagina(String nombrePagina) {
        jButton2.setBackground(new java.awt.Color(240, 241, 241));
        jButton6.setBackground(new java.awt.Color(240, 241, 241));
        jButton7.setBackground(new java.awt.Color(240, 241, 241));
        jButton8.setBackground(new java.awt.Color(240, 241, 241));
        jButton9.setBackground(new java.awt.Color(240, 241, 241));
        jButton10.setBackground(new java.awt.Color(240, 241, 241));
        switch (nombrePagina) {
            case "inicio" -> {
                jButton2.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            case "prestamo" -> {
                jButton6.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            case "devolucion" -> {
                jButton7.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            case "reporte" -> {
                jButton8.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            case "usuarios" -> {
                jButton9.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            case "empleados" -> {
                jButton10.setBackground(new java.awt.Color(230, 231, 231));
                break;
            }
            default -> {
                break;
            }
        }
    }

