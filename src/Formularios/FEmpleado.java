    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nombre = TxtNombre.getText();
        String DNI = TxtDNI.getText();
        String Apellidos = TxtApellidos.getText();
        String Direccion = TxtDireccion.getText();
        String Telefono = TxtTelefono.getText();
        String Cargo = TxtCargo.getText();
        float salario = Float.parseFloat(TxtSalario.getText());
        Empleado jefe = new Empleado();
        if (!TxtIdJefe.getText().isEmpty()) {
            try {
                jefe.setId(Integer.parseInt(TxtIdJefe.getText()));
            } catch (NumberFormatException e) {
                // Manejar la excepción si el texto no es un número válido
                System.out.println("Error: El texto no es un número válido.");
            }
        }

        if (DNI.isEmpty() || nombre.isEmpty() || Apellidos.isEmpty() || Direccion.isEmpty() || Telefono.isEmpty() || Cargo.isEmpty() || TxtSalario.getText().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            TxtDNI.requestFocus();
        }
        Empleado e1 = new Empleado(DNI, nombre, Apellidos, Direccion, Telefono, Cargo, salario, jefe);
        try {
            BDEmpleados bdEmpleados = new BDEmpleados();
            if (this.idEditar != -1) {
                e1.setId(this.idEditar);
                bdEmpleados.modificar(e1);
                limpiarCampos();
                irAEmpleados();
                return;
            }
            bdEmpleados.registrar(e1);
            limpiarCampos();
            irAEmpleados();
        } catch (Exception e) {
            String errorMsg = this.idEditar != -1 ? "Modificar" : "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al " + errorMsg + " el usuario. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

