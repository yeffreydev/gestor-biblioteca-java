    public void setIdEditar(int id) {
        this.idEditar = id;
        try {
            BDEmpleados bdEmpleados = new BDEmpleados();
            Empleado empleado = bdEmpleados.getEmpleadoById(id);
            TxtDNI.setText(empleado.getDni());
            TxtNombre.setText(empleado.getNombre());
            TxtApellidos.setText(empleado.getApellidos());
            TxtDireccion.setText(empleado.getDireccion());
            TxtTelefono.setText(empleado.getTelefono());
            TxtCargo.setText(empleado.getCargo());
            TxtSalario.setText(String.valueOf(empleado.getSalario()));
            TxtIdJefe.setText(empleado.getJefe() != null ? String.valueOf(empleado.getJefe().getId()) : "NULL");
        } catch (Exception e) {
            System.out.println(e);
        }
    }

