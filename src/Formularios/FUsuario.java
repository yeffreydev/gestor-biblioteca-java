    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String dni = TxtDNI.getText();
        String nombre = TxtName.getText();
        String apellidos = TxtApellidos.getText();
        String direccion = TxtDireccion.getText();
        String telefono = TxtTelefono.getText();
        String email = TxtEmail.getText();

        if (dni.isEmpty() || nombre.isEmpty() || apellidos.isEmpty() || direccion.isEmpty() || telefono.isEmpty() || email.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            TxtDNI.requestFocus();
        }
        Usuario u;
        
         u = new Usuario(dni, nombre, apellidos, direccion, telefono, email);

        try {
            BDUsuarios bdUsuarios = new BDUsuarios();
            if (this.idEditar!=-1) {
                u.setId(this.idEditar);
                bdUsuarios.modificar(u);
                limpiarCampos();
                irAUsuarios();
                return;
            }
            bdUsuarios.registrar(u);
            limpiarCampos();
            irAUsuarios();
        } catch (Exception e) {
            String errorMsg = this.idEditar !=-1 ? "Modificar": "registrar";
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al " + errorMsg + " el usuario. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }

        //
    }//GEN-LAST:event_jButton1ActionPerformed

