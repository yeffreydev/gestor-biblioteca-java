    public void setIdEditar(int id) {
        this.idEditar = id;
        try {
            BDUsuarios bdUsuarios = new BDUsuarios();
            Usuario user = bdUsuarios.getUserById(id);
            TxtDNI.setText(user.getDni());
            TxtName.setText(user.getNombre());
            TxtApellidos.setText(user.getApellidos());
            TxtDireccion.setText(user.getDireccion());
            TxtTelefono.setText(user.getTelefono());
            TxtEmail.setText(user.getEmail());
        } catch (Exception e) {
            System.out.println(e);
        }
    }

